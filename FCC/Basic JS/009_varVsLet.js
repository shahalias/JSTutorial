// Explore Differences Between the var and let Keywords

// var keyword is that you can easily overwrite variable declarations
// var camper = "James";
// var camper = "David";

// camper variable is originally declared as James, and is then overridden to be David

// let was introduced in ES6, a major update to JavaScript, to solve this potential issue with the var keyword
// var with let in the code above, it results in an error

// So unlike var, when you use let, a variable with the same name can only be declared once.

let catName = "Oliver";
let catSound = "Meow!";